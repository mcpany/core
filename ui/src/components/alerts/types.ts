/**
 * Defines the severity level of an alert.
 * - `critical`: Requires immediate attention.
 * - `warning`: Potential issue or degraded performance.
 * - `info`: Informational event.
 */
export type Severity = "critical" | "warning" | "info";

/**
 * Defines the lifecycle status of an alert.
 * - `active`: The issue is currently ongoing.
 * - `acknowledged`: The issue is being investigated.
 * - `resolved`: The issue has been fixed.
 */
export type AlertStatus = "active" | "acknowledged" | "resolved";

/**
 * Represents an active alert within the system.
 * Alerts are generated when specific conditions or rules are triggered by service events.
 */
export interface Alert {
  /** The unique identifier for the alert. */
  id: string;
  /** A short, descriptive title of the alert. */
  title: string;
  /** The detailed error message or event description. */
  message: string;
  /** The severity level of the alert. */
  severity: Severity;
  /** The current status of the alert. */
  status: AlertStatus;
  /** The ID of the service that generated the alert. */
  service: string;
  /** The timestamp when the alert was created (ISO 8601 format). */
  timestamp: string;
  /** The source component or module that triggered the alert. */
  source: string;
}

/**
 * Defines a rule for generating alerts based on system conditions.
 */
export interface AlertRule {
  /** The unique identifier for the rule. */
  id: string;
  /** The human-readable name of the rule. */
  name: string;
  /** The condition expression that triggers the alert (e.g., "latency > 100ms"). */
  condition: string;
  /** The severity level to assign to alerts generated by this rule. */
  severity: Severity;
  /** The ID of the service to which this rule applies. */
  service: string;
  /** Whether the rule is currently active. */
  enabled: boolean;
}
