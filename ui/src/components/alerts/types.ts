/**
 * Defines the severity level of an alert.
 *
 * - `critical`: Requires immediate attention.
 * - `warning`: Something is wrong but service is still operational.
 * - `info`: Informational message.
 */
export type Severity = "critical" | "warning" | "info";

/**
 * Defines the current status of an alert.
 *
 * - `active`: The alert is currently firing.
 * - `acknowledged`: The alert has been seen but not resolved.
 * - `resolved`: The issue causing the alert has been fixed.
 */
export type AlertStatus = "active" | "acknowledged" | "resolved";

/**
 * Represents an alert triggered by the system.
 */
export interface Alert {
  /** Unique identifier for the alert. */
  id: string;
  /** Short summary of the alert. */
  title: string;
  /** Detailed description of what happened. */
  message: string;
  /** The severity level of the alert. */
  severity: Severity;
  /** The current lifecycle status of the alert. */
  status: AlertStatus;
  /** The name of the service that generated the alert. */
  service: string;
  /** ISO 8601 timestamp of when the alert was created. */
  timestamp: string;
  /** The source component identifying where the alert originated. */
  source: string;
}

/**
 * Defines a rule for generating alerts based on conditions.
 */
export interface AlertRule {
  /** Unique identifier for the rule. */
  id: string;
  /** Human-readable name for the rule. */
  name: string;
  /** Expression or condition that triggers the alert. */
  condition: string;
  /** The severity level assigned to alerts generated by this rule. */
  severity: Severity;
  /** The target service this rule applies to. */
  service: string;
  /** Whether the rule is currently active. */
  enabled: boolean;
}
